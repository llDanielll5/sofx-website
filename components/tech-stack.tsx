export function TechStack() {
  const [isVisible, setIsVisible] = useState(false);
  const sectionRef = useRef<HTMLElement>(null);
  const scrollRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
        }
      },
      { threshold: 0.1 },
    );

    if (sectionRef.current) {
      observer.observe(sectionRef.current);
    }

    return () => observer.disconnect();
  }, []);

  // Auto-scroll animation
  useEffect(() => {
    const scrollContainer = scrollRef.current;
    if (!scrollContainer) return;

    let animationId: number;
    let scrollPosition = 0;
    const speed = 0.5;

    const animate = () => {
      scrollPosition += speed;
      if (scrollPosition >= scrollContainer.scrollWidth / 2) {
        scrollPosition = 0;
      }
      scrollContainer.scrollLeft = scrollPosition;
      animationId = requestAnimationFrame(animate);
    };

    animationId = requestAnimationFrame(animate);

    return () => cancelAnimationFrame(animationId);
  }, []);

  return (
    <section
      id="tecnologias"
      ref={sectionRef}
      className="relative py-18 md:py-24 overflow-hidden"
    >
      {/* Background */}
      <div className="absolute inset-0 bg-[#0a0a12]" />
      <div className="absolute inset-0 grid-pattern opacity-20" />

      <div className="container mx-auto px-4 md:px-6 relative z-10">
        {/* Section Header */}
        <div
          className={`text-center max-w-3xl mx-auto mb-16 transition-all duration-700 ${
            isVisible ? "opacity-100 translate-y-0" : "opacity-0 translate-y-10"
          }`}
        >
          <span className="inline-block px-4 py-1 rounded-full border border-[#6366f1]/30 bg-[#6366f1]/5 text-[#6366f1] text-sm font-medium mb-4">
            Tech Stack
          </span>
          <h2 className="text-3xl md:text-5xl font-bold font-[var(--font-poppins)] mb-6">
            <span className="text-[#f0f0f5]">Tecnologias que </span>
            <span className="text-transparent bg-clip-text bg-gradient-to-r from-[#00d4ff] to-[#6366f1]">
              dominamos
            </span>
          </h2>
          <p className="text-lg text-[#a0a0b0] leading-relaxed">
            Utilizamos as mais modernas tecnologias do mercado para garantir
            qualidade e performance em todos os projetos.
          </p>
        </div>

        {/* Infinite Scroll Carousel */}
        <div
          className={`relative overflow-hidden transition-all duration-700 delay-300 ${
            isVisible ? "opacity-100" : "opacity-0"
          }`}
        >
          {/* Gradient Masks */}
          <div className="absolute left-0 top-0 bottom-0 w-32 bg-gradient-to-r from-[#0a0a12] to-transparent z-10 pointer-events-none" />
          <div className="absolute right-0 top-0 bottom-0 w-32 bg-gradient-to-l from-[#0a0a12] to-transparent z-10 pointer-events-none" />

          <div
            ref={scrollRef}
            className="flex gap-8 overflow-x-hidden py-8"
            style={{ scrollbarWidth: "none", msOverflowStyle: "none" }}
          >
            {/* Duplicate technologies for infinite scroll effect */}
            {[...technologies, ...technologies].map((tech, index) => (
              <div
                key={`${tech.name}-${index}`}
                className="flex-shrink-0 group"
              >
                <div className="w-24 h-24 rounded-xl bg-[#12121a] border border-[#1a1a2e] flex flex-col items-center justify-center gap-2 transition-all duration-300 hover:border-[#00d4ff]/50 hover:bg-[#00d4ff]/5 cursor-default">
                  <div className="text-[#a0a0b0] group-hover:text-[#00d4ff] transition-colors duration-300">
                    {tech.icon}
                  </div>
                </div>
                <p className="text-center text-xs text-[#a0a0b0] mt-2 group-hover:text-[#f0f0f5] transition-colors">
                  {tech.name}
                </p>
              </div>
            ))}
          </div>
        </div>
      </div>
    </section>
  );
}
